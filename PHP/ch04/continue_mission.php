<?php
    /* 
        for문 노터치, continue를 활용하여 홀수만 출력
        1
        3
        5
        7
        ...
        99
    */
    for($i=1; $i <=100; $i++)
    {
        if($i % 2 == 0) {continue;}
        print $i . "<br>";
    }

    print "====================================<br>";

    //자체 문제) 1~100까지 3의 배수만 나오지 않도록 출력
    for($i=1; $i <=100; $i++)
    {
        if($i % 3 == 0) {continue;}
        print $i . "<br>";
    }



?>